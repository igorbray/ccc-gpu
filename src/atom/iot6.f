      SUBROUTINE IOT6(L1,L2,L3,L4,L5,L6,B)
      REAL*8B,SUM
      B=AMOD1(L1,L2,L3)*AMOD1(L1,L5,L6)*
     *AMOD1(L2,L4,L6)*AMOD1(L3,L4,L5)
      SUM=0.0
      LZ=0
   10 LC1=IFACT(LZ-L1-L2-L3)*IFACT(LZ-L1-L5-L6)*
     *IFACT(LZ-L4-L2-L6)*IFACT(LZ-L4-L5-L3)
      IF(LC1.EQ.0) GOTO 20
      LC2=IFACT(L1+L2+L4+L5-LZ)*IFACT(L2+L3+
     *L5+L6-LZ)*IFACT(L3+L1+L6+L4-LZ)
      IF(LC2.EQ.0 )GOTO 20
      SUM=SUM+(-1.)**LZ*
     *DFLOAT( IFACT(LZ+1))/DFLOAT(LC1*LC2)
   20 LZ=LZ+1
      IF(SUM.NE.0..AND.LC1.NE.0.
     *AND.LC2.EQ.0) GOTO 30
      GOTO10
   30 B=B*SUM
      RETURN
      END
